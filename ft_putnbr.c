#include "libft.h"

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		ft_putstr("-2147483648" + 0);
	}
	else
	{
		if (nb < 0)
		{
			ft_putchar('-');
			nb = nb * (-1);
		}
		if (nb >= 10)
			ft_putnbr(nb / 10);
		if (nb <= 0)
			ft_putchar(-(nb % 10) + '0');
		else
			ft_putchar(nb % 10 + '0');
	}
}
